<p>状态码分类</p>
<ul>
<li>1xx 服务器收到请求</li>
<li>2xx 请求成功，如 200</li>
<li>3xx 重定向，如 302</li>
<li>4xx 客户端错误，如 404</li>
<li>500 服务端错误，如 500</li>
</ul>
<p>常用状态码</p>
<ul>
<li>200 成功</li>
<li>301 永久重定向（服务端返回新的 location 地址，浏览器自动处理）</li>
<li>302 临时重定向（服务端返回新的 location 地址，浏览器自动处理）</li>
<li>304 资源未被修改</li>
<li>404 资源未找到</li>
<li>403 没有权限</li>
<li>500 服务器错误</li>
<li>504 网关超时</li>
</ul>
